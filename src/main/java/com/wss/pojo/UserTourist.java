package com.wss.pojo;

public class UserTourist {
    private Integer touristId;

    private String touristName;

    private String touristTel;

    private String touristCard;

    private String touristPsw;

    public Integer getTouristId() {
        return touristId;
    }

    public void setTouristId(Integer touristId) {
        this.touristId = touristId;
    }

    public String getTouristName() {
        return touristName;
    }

    public void setTouristName(String touristName) {
        this.touristName = touristName;
    }

    public String getTouristTel() {
        return touristTel;
    }

    public void setTouristTel(String touristTel) {
        this.touristTel = touristTel;
    }

    public String getTouristCard() {
        return touristCard;
    }

    public void setTouristCard(String touristCard) {
        this.touristCard = touristCard;
    }

    public String getTouristPsw() {
        return touristPsw;
    }

    public void setTouristPsw(String touristPsw) {
        this.touristPsw = touristPsw;
    }
}