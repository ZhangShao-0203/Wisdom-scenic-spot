package com.wss.pojo;

public class PublicIntroduce {
    private Integer introduceId;

    private String introduceName;

    private String introducePhone;

    private String introduceImgs;

    private String introduceMsg;

    private Integer Page;

    public Integer getPage() {
        return Page;
    }

    public void setPage(Integer page) {
        Page = page;
    }

    public Integer getIntroduceId() {
        return introduceId;
    }

    public void setIntroduceId(Integer introduceId) {
        this.introduceId = introduceId;
    }

    public String getIntroduceName() {
        return introduceName;
    }

    public void setIntroduceName(String introduceName) {
        this.introduceName = introduceName;
    }

    public String getIntroducePhone() {
        return introducePhone;
    }

    public void setIntroducePhone(String introducePhone) {
        this.introducePhone = introducePhone;
    }

    public String getIntroduceImgs() {
        return introduceImgs;
    }

    public void setIntroduceImgs(String introduceImgs) {
        this.introduceImgs = introduceImgs;
    }

    public String getIntroduceMsg() {
        return introduceMsg;
    }

    public void setIntroduceMsg(String introduceMsg) {
        this.introduceMsg = introduceMsg;
    }
}